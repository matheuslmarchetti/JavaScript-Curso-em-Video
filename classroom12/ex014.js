var now = new Date()
var dayOfTheWeek = now.getDay()
switch (dayOfTheWeek) {
    case 0:
        console.log("Sunday");
        break;
    case 1:
        console.log("Monday");
        break;
    case 2:
        console.log("Tuesday");
        break;
    case 3:
        console.log("Tuesday");
        break;
    case 4:
        console.log("Thursday");
        break;
    case 5:
        console.log("Friday");
        break;
    case 6:
        console.log("Saturday");
    default:
        console.log("Error! Invalid Day");
        break;
}